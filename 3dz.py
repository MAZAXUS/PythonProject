if grade == 1:
    print("Плохо")
elif grade == 2:
    print("Удовлетворительно")
elif grade == 3:
    print("Хорошо")
elif grade == 4:
    print("Отлично")
else:
    print("Неверная оценка")

#Напиши программу, которая проверяет одновременно два условия для числа num:
#Число должно быть положительным (> 0).
#Число должно быть чётным.
#Если оба условия выполнены, выводи "Подходит".
#Если хотя бы одно не выполнено — "Не подходит".

#Требование: Используй логический оператор and.

num = 10
if num > 0 and num % 2 == 0:
    print("Подходит")
else:
    print("Не подходит")

