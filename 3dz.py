#Напиши программу, которая по оценке grade (от 1 до 5) выводит:
#1 → "Плохо"
#2 → "Удовлетворительно"
#3 → "Хорошо"
#4 → "Отлично"
#5 → "Неверная оценка" (если больше 5 или меньше 1).

